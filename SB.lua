//Security Bureau's Information\\:

Our main job within the Security Bureau is to protect the base, making sure no hostiles manage to gain entry into it.
We have 3 branches within the Bureau. They are Training Section, Administrative Affairs, and Standard Patrol Division.
Our current Chief of Security is EliAroze, and the Assistant Chief of Security is Colewrid.
The Security Bureau has 1 official subdivision. SCU, Strategic Control Unit contains the Bureau's most skilled players, and is also our last line of defense.